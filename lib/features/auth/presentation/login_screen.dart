// lib/login_screen.dart
import 'package:flutter/material.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'register_screen.dart'; // Para navegar para a tela de registro

class LoginScreen extends StatefulWidget {
	const LoginScreen({super.key});

	@override
	State<LoginScreen> createState() => _LoginScreenState();
}

class _LoginScreenState extends State<LoginScreen> {
	final _emailController = TextEditingController();
	final _passwordController = TextEditingController();
	final _formKey = GlobalKey<FormState>();
	bool _isLoading = false;

	Future<void> _signIn() async {
		if (!_formKey.currentState!.validate()) return;

		setState(() {
			_isLoading = true;
		});

		try {
			await FirebaseAuth.instance.signInWithEmailAndPassword(
				email: _emailController.text.trim(),
				password: _passwordController.text.trim(),
			);
			// A navegação para HomeScreen será feita pelo AuthGate
		} on FirebaseAuthException catch (e) {
			String message;
			if (e.code == 'user-not-found') {
			message = 'Nenhum usuário encontrado para este e-mail.';
			} else if (e.code == 'wrong-password') {
				message = 'Senha incorreta.';
			} else if (e.code == 'invalid-email') {
				message = 'O formato do e-mail é inválido.';
			} else {
				message = 'Ocorreu um erro. Tente novamente.';
				print('Erro de login: ${e.code} - ${e.message}');
			}
			if (mounted) {
				ScaffoldMessenger.of(context).showSnackBar(
					SnackBar(content: Text(message), backgroundColor: Colors.red),
				);
			}
		} catch (e) {
			if (mounted) {
				ScaffoldMessenger.of(context).showSnackBar(
					SnackBar(
						content: Text('Ocorreu um erro inesperado: $e'),
						backgroundColor: Colors.red,
					),
				);
			}
		} finally {
			if (mounted) {
				setState(() {
					_isLoading = false;
				});
			}
		}
	}

	@override
	void dispose() {
		_emailController.dispose();
		_passwordController.dispose();
		super.dispose();
	}

	@override
	Widget build(BuildContext context) {
		return Scaffold(
			appBar: AppBar(title: const Text('Login - Firy Streak')),
			body: Center(
				child: SingleChildScrollView(
					padding: const EdgeInsets.all(100.0),
					child: Form(
						key: _formKey,
						child: Column(
							mainAxisAlignment: MainAxisAlignment.center,
							crossAxisAlignment: CrossAxisAlignment.stretch,
							children: <Widget>[
								Text(
									'Seja bem vindo novamente!!',
									style: Theme.of(context).textTheme.headlineSmall,
									textAlign: TextAlign.center,
								),
								const SizedBox(height: 30),
								TextFormField(
									controller: _emailController,
									decoration: const InputDecoration(labelText: 'Email'),
									keyboardType: TextInputType.emailAddress,
									validator: (value) {
										if (value == null || value.isEmpty) {
											return 'Por favor, insira seu email';
										}
										if (!value.contains('@')) {
											return 'Email inválido';
										}
										return null;
									},
								),
								const SizedBox(height: 16),
								TextFormField(
									controller: _passwordController,
									decoration: const InputDecoration(labelText: 'Senha'),
									obscureText: true,
									validator: (value) {
										if (value == null || value.isEmpty) {
											return 'Por favor, insira sua senha';
										}
										return null;
									},
								),
								const SizedBox(height: 24),
								_isLoading
										? const Center(child: CircularProgressIndicator())
										: ElevatedButton(
												onPressed: _signIn,
												child: const Text('Entrar'),
											),
								const SizedBox(height: 16),
								TextButton(
									onPressed: () {
										Navigator.of(context).push(
											MaterialPageRoute(
												builder: (context) => const RegisterScreen(),
											),
										);
									},
									child: const Text('Não tem uma conta? Cadastre-se'),
								),
							],
						),
					),
				),
			),
		);
	}
}
